/*
|--------------------------------------------------------------------------
| 后端应用服务
|--------------------------------------------------------------------------
| 暴露可由 Vue 前端通过 Wails 调用的 Go 方法。
|--------------------------------------------------------------------------
*/
package main

import "context"

/*
|--------------------------------------------------------------------------
| 模块能力清单
|--------------------------------------------------------------------------
| 生命周期管理：保存 Wails 上下文，供本地文件对话框和运行时能力使用。
| 模型分析桥接：把前端请求转发给本地三维模型分析服务。
| 预览文件读取：为前端 Three.js 预览提供可读取的本地模型文件内容。
| 上传文件读取：为前端上传云端提供一次性内存载荷，不在本地复制模型。
|--------------------------------------------------------------------------
*/

/*
|--------------------------------------------------------------------------
| 应用状态结构
|--------------------------------------------------------------------------
| 保存 Wails 运行时上下文和本地模型分析服务。
|--------------------------------------------------------------------------
*/
type App struct {
	ctx      context.Context
	analyzer *ModelAnalyzer
}

/*
|--------------------------------------------------------------------------
| 应用实例创建
|--------------------------------------------------------------------------
| 初始化后端应用服务，并交给 Wails 绑定到前端调用对象。
|--------------------------------------------------------------------------
*/
func NewApp() *App {
	return &App{
		analyzer: NewModelAnalyzer(),
	}
}

/*
|--------------------------------------------------------------------------
| Wails 启动生命周期
|--------------------------------------------------------------------------
| 应用启动时保存上下文，后续可用于打开文件、弹窗或调用运行时能力。
|--------------------------------------------------------------------------
*/
func (a *App) Startup(ctx context.Context) {
	a.ctx = ctx
}

/*
|--------------------------------------------------------------------------
| 打开并分析本地模型文件
|--------------------------------------------------------------------------
| 前端点击“导入文件”时调用，打开本地文件选择器并返回模型分析结果。
|--------------------------------------------------------------------------
*/
func (a *App) ImportModelFiles() ([]ModelAnalysis, error) {
	return a.analyzer.ImportModelFiles(a.ctx)
}

/*
|--------------------------------------------------------------------------
| 分析指定本地模型文件
|--------------------------------------------------------------------------
| 前端或后续服务端流程传入路径后调用，返回该文件的格式和基础几何信息。
|--------------------------------------------------------------------------
*/
func (a *App) AnalyzeModelFile(path string) (ModelAnalysis, error) {
	return a.analyzer.AnalyzeFile(path)
}

/*
|--------------------------------------------------------------------------
| 读取可预览模型文件
|--------------------------------------------------------------------------
| 前端需要用 Three.js 预览本地 STL、OBJ、FBX 文件时调用。
|--------------------------------------------------------------------------
*/
func (a *App) ReadPreviewModel(path string) (PreviewModelPayload, error) {
	return a.analyzer.ReadPreviewModel(path)
}

/*
|--------------------------------------------------------------------------
| 读取上传模型文件
|--------------------------------------------------------------------------
| 前端点击“上传云端”时调用，把本地模型读成一次性 Base64 载荷用于上传。
|--------------------------------------------------------------------------
*/
func (a *App) ReadModelForUpload(path string) (UploadModelPayload, error) {
	return a.analyzer.ReadModelForUpload(path)
}
